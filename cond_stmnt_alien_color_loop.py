alien_color = ['orange', 'red', 'green']
for color in alien_color:
    if color == 'orange':
        print("You have Earned 5 points")
    elif color == 'red':
        print("You have Earned 10 Points")
    elif color == "green" :
        print("You have Earned 15 Points")
    else:
        print("You have earned 0 points")