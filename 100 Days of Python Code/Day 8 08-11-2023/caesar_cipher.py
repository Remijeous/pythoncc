def caesar_ciphar(start_text, shift_amount, ciphar_direction):
    end_text = ""
    if ciphar_direction == "decode":
        shift_amount *= -1
    for char in start_text:
        if char in alphabet:
            position = alphabet.index(char)
            new_position = position + shift_amount
            end_text += alphabet[new_position]
        else:
            end_text += char
    print(f"Here's the {ciphar_direction}d text: {end_text}")

